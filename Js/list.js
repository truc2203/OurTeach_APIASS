function Lists(
  account,
  userName,
  password,
  email,
  type,
  language,
  desc,
  avatar,
  id
) {
  (this.account = account),
    (this.userName = userName),
    (this.password = password),
    (this.email = email),
    (this.type = type),
    (this.language = language),
    (this.desc = desc),
    (this.avatar = avatar),
    (this.id = id);
}
